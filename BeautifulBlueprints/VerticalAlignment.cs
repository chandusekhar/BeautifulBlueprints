
namespace BeautifulBlueprints
{
    public enum VerticalAlignment
    {
        /// <summary>
        /// Align elements to the top, i.e. put all spare space to the bottom
        /// </summary>
        Top,

        /// <summary>
        /// Align elements to the bottom, i.e. put all spare space to the top
        /// </summary>
        Bottom,

        /// <summary>
        /// Align all elements to the center, i.e. put all spare space equally on the top and bottom
        /// </summary>
        Center,
    }
}
