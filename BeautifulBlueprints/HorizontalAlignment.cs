
namespace BeautifulBlueprints
{
    public enum HorizontalAlignment
    {
        /// <summary>
        /// Align elements to the left, i.e. put all spare space to the right
        /// </summary>
        Left,

        /// <summary>
        /// Align elements to the right, i.e. put all spare space to the left
        /// </summary>
        Right,

        /// <summary>
        /// Align all elements to the center, i.e. put all spare space equally on the left and right
        /// </summary>
        Center,

        /// <summary>
        /// Align elements unformly along the axis, i.e. put all spare space evenly between every element
        /// </summary>
        Uniform
    }
}
